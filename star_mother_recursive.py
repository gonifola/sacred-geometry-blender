def make_material(name, r, g, b, alpha=1.0, metallic=0.5, roughness=0.2):
    mat = bpy.data.materials.new(name)
    mat.use_nodes = True
    bsdf = mat.node_tree.nodes["Principled BSDF"]
    bsdf.inputs['Base Color'].default_value = (r, g, b, 1.0)
    bsdf.inputs['Metallic'].default_value = metallic
    bsdf.inputs['Roughness'].default_value = roughness
    if alpha < 1.0:
        bsdf.inputs['Alpha'].default_value = alpha
        mat.blend_method = 'BLEND'
        mat.shadow_method = 'NONE'
    return mat

for shell_name, pp, rgb, alpha, metallic, roughness in SHELLS:
    scale = outer_scale * phi**pp
    verts_s = [(x*scale, y*scale, z*scale) for x,y,z in verts_base]
    mesh = bpy.data.meshes.new(shell_name)
    mesh.from_pydata(verts_s, [], faces)
    mesh.update(calc_edges=True)
    mesh.validate()
    obj = bpy.data.objects.new(shell_name, mesh)
    bpy.context.collection.objects.link(obj)
    mat = make_material(shell_name+'_mat', rgb[0], rgb[1], rgb[2],
                        alpha=alpha, metallic=metallic, roughness=roughness)
    obj.data.materials.append(mat)
    print(f'Shell {shell_name}: R={R_base*scale:.2f}u')

